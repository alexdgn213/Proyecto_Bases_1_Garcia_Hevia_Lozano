    private void tablaUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaUsuariosMouseClicked
        int fila = tablaUsuarios.getSelectedRow();
        if (fila>=0){
            int id = (Integer) tablaUsuarios.getValueAt(fila, 0);
            u = Usuario.buscarPorCodigo(conector, id);
            jtfNombre.setText(String.valueOf(u.getUsu_nombre()));
            jcbRol.setSelectedIndex(u.getFk_rol_codigo());
        }
    }//GEN-LAST:event_tablaUsuariosMouseClicked

    private void bDelInf1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bDelInf1ActionPerformed
        if(u!=null){
            if(jcbRol.getSelectedIndex()>0){
                u.setFk_rol_codigo(Rol.obtenerCodigoPorNombre(conector, jcbRol.getSelectedItem().toString())); 
                u.modificarEnDB(conector);
                new Thread(new MensajeUI(panelMensaje,"Se ha cambiado el rol exitosamente",1)).start();
            }
            else{
                new Thread(new MensajeUI(panelMensaje,"No se ha seleccionado ningun rol",0)).start();
            }
        }
        else{
            new Thread(new MensajeUI(panelMensaje,"No se ha seleccionado ningun usuario",0)).start();
        }
        Usuario.llenarTabla(conector, tablaUsuarios);
        
    }//GEN-LAST:event_bDelInf1ActionPerformed


