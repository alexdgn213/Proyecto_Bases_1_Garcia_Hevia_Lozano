    private void jbModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbModificarActionPerformed
        int fila = tablaRoles.getSelectedRow();
        if (fila>=0){
            int id = (Integer) tablaRoles.getValueAt(fila, 0);
            String modelo = tablaRoles.getValueAt(fila, 1).toString();
            String cliente = tablaRoles.getValueAt(fila, 2).toString();
            DetalleAvion nuevoPanel = new DetalleAvion(conector,contenedor,id,modelo,cliente,panelMensaje,false);
            contenedor.removeAll();
            contenedor.add(nuevoPanel);
            contenedor.updateUI();
        }
    }//GEN-LAST:event_jbModificarActionPerformed

    private void bDelInfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bDelInfActionPerformed
        if(u!=null){
            u.eliminarDeDB(conector);
            new Thread(new MensajeUI(panelMensaje,"Se elimino correctamente el usuario",1)).start();
        }
        else{
            new Thread(new MensajeUI(panelMensaje,"No se ha seleccionado ningun usuario",0)).start();
        }
        Usuario.llenarTabla(conector, tablaUsuarios);
        
    }//GEN-LAST:event_bDelInfActionPerformed

